package javatrainning.flowcontrol;

public class Condition {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 5;

        switch (num2){
            case 1:
                System.out.println(45);
                break;
            case 5:
                System.out.println(20);
                break;
            default:
                System.out.println(0);

        }

    }
}
